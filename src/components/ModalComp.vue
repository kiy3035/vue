<template>
  <div class="modal" v-if="showModal">
    <div class="modal-content">
      <span class="close" @click="closeModal">&times;</span>
      <!-- 모달 내용 -->
      <p>This is a modal!</p>
    </div>
  </div>
</template>

<script>
console.log("모달창입니다")
export default {
  data() {
    return {
      showModal: true,
    };
  },
  methods: {
    openModal() {
      this.showModal = true;
    },
    closeModal() {
      this.showModal = false;
    },
  },
};
</script>

